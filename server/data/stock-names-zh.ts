/**
 * 获取股票的中文名称
 * @param symbol 股票代码（如 0325.HK, 600519.SH）
 * @returns 中文名称，如果没有映射则返回null
 */
export function getChineseStockName(symbol: string): string | null {
  // 标准化股票代码格式
  const normalizedSymbol = symbol.toUpperCase();
  
  // 查找港股
  if (normalizedSymbol.endsWith('.HK')) {
    return HK_STOCK_NAMES[normalizedSymbol] || null;
  }
  
  // 查找A股
  if (normalizedSymbol.endsWith('.SH') || normalizedSymbol.endsWith('.SZ')) {
    return CN_STOCK_NAMES[normalizedSymbol] || null;
  }
  
  // 尝试添加后缀查找
  const hkSymbol = normalizedSymbol.padStart(4, '0') + '.HK';
  if (HK_STOCK_NAMES[hkSymbol]) {
    return HK_STOCK_NAMES[hkSymbol];
  }
  
  return null;
}

/**
 * 根据股票代码和市场信息获取显示名称
 * @param symbol 股票代码
 * @param longName API返回的英文长名称
 * @param shortName API返回的英文短名称
 * @returns 优先返回中文名称，否则返回英文名称
 */
export function getDisplayName(
  symbol: string,
  longName?: string,
  shortName?: string
): string {
  const chineseName = getChineseStockName(symbol);
  if (chineseName) {
    return chineseName;
  }
  
  return longName || shortName || symbol;
}
